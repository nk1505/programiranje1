function dan_v_tednu(dan) {
    if (dan === 0) {
        return "Nedelja";
    } else if (dan === 1) {
        return "Ponedeljek";
    } else if (dan === 2) {
        return "Torek";
    } else if (dan === 3) {
        return "Sreda";
    } else if (dan === 4) {
        return "ÄŒetrtek";
    } else if (dan === 5) {
        return "Petek";
    } else {
        return "Sobota";
    }
}
